<h4>Have you ever given a presentation, looked out to the crowd, only to find a bunch of confused faces?
<br/><b>PresentPoint</b> aims to give the presenter live feedback through Twitter based voting.</h4>
The first step to gaining presentation cognizance is to create a Presentation Room. Then, define unique hashtags that relate to your main presentation points. When your audience becomes confused, or wants clarification on a point, they can tweet that hashtag. As users tweet the various hashtags, the circles in your presentation dash will grow larger and brighter, which gives you immediate and clear feedback. 
